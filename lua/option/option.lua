-- file format

vim.opt.fileformat = "unix"
vim.opt.enc = "utf-8"
vim.opt.fenc = "utf-8"

-- view
vim.opt.number = true
vim.opt.signcolumn = "yes"
vim.opt.laststatus = 2
vim.opt.clipboard:append { "unnamed,unnamedplus" }
vim.opt.mouse = "a"
vim.opt.expandtab = true
vim.opt.syntax = "on"

-- tabstop
vim.opt.tabstop = 4
vim.opt.shiftwidth = 4

